
// ----------------------------------------Recepcion
{

let caja= document.getElementById("recepcion")

caja.addEventListener("click",()=>{

let mensaje_= document.getElementById('caja-recepcion')
mensaje_.classList.add("caja_visible")

})

}

{
let cerrar= document.getElementById('Cerrar_')

cerrar.addEventListener("click",()=>{

let ventana= document.getElementById('caja-recepcion')

ventana.classList.remove("caja_visible")

})

}
